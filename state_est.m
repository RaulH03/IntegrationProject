R1 = diag([1e-1, 1e-1, 1e-1, 1e-1]);

R2 = diag([1e-1, 1e-1]);

function  [Pk_k, xk_k] = Kalman_correct(yk, Pk_km1, xk_km1)
    K_k = Pk_km1*C'/(C*Pk_km1*C' + R2);
    xk_k = xk_km1 + K_k*(yk - C*xk_km1);
    Pk_k = Pk_km1 - K_k*C*Pk_km1;
end

function [Pk_kp1, xk_kp1] = Kalman_pred(uk, Pk_k, xk_k)
    xk_kp1 = A * xk_k + B * uk;
    Pk_kp1 = A*Pk_k*A' + R1;
end


